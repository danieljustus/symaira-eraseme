def generate_scheduler_configs(
    platform_name: str = "",
    *,
    output_dir: str = "./schedules",
    tick_hour: int = TICK_DEFAULT_TIME.hour,
    tick_minute: int = TICK_DEFAULT_TIME.minute,
    poll_hours: list[int] | None = None,
    project_dir: str = "",
    openeraseme_bin: str = "",
    venv_activate: str = "",
) -> dict[str, str]:
    """Generate scheduler config files for the given platform.

    Args:
        platform_name: One of "cron", "launchd", "systemd", or "" for auto-detect.
        output_dir: Directory to write files into.
        tick_hour: Hour for daily tick (0-23).
        tick_minute: Minute for daily tick (0-59).
        poll_hours: Hours for poll-inbox runs (default: 8, 12, 16, 20).
        project_dir: Project directory for wrapper scripts.
        openeraseme_bin: Path to openeraseme binary.
        venv_activate: Path to virtualenv activate script.

    Returns:
        Dict mapping relative filename -> content.
    """
    if not platform_name:
        platform_name = detect_platform()

    platform_name = platform_name.lower()
    if platform_name not in ("cron", "launchd", "systemd"):
        msg = f"Unsupported platform: {platform_name}. Choose cron, launchd, or systemd."
        raise ValueError(msg)

    poll_times_list = [time(h, 0) for h in (poll_hours or [8, 12, 16, 20])]

    config = SchedulerConfig(
        tick_time=time(tick_hour, tick_minute),
        poll_times=poll_times_list,
        openeraseme_bin=openeraseme_bin or _resolve_bin(),
        venv_activate=venv_activate or _resolve_venv(),
        output_dir=output_dir,
        platform=platform_name,
    )

    generators = {
        "cron": generate_cron,
        "launchd": generate_launchd,
        "systemd": generate_systemd,
    }

    generator = generators[platform_name]
    return generator(config, project_dir=project_dir)


def write_scheduler_files(
    files: dict[str, str],
    output_dir: str,
    *,
    dry_run: bool = False,
) -> list[str]:
    """Write generated scheduler files to disk.

    Args:
        files: Dict of filename -> content from generate_* functions.
        output_dir: Directory to write files into.
        dry_run: If True, only print what would be written.

    Returns:
        List of written file paths.
    """
    out_dir = Path(output_dir).expanduser().resolve()
    written: list[str] = []

    for filename, content in files.items():
        dest = out_dir / filename
        if dry_run:
            logger.info("[dry-run] Would write: %s (%d bytes)", dest, len(content))
            written.append(str(dest))
            continue

        dest.parent.mkdir(parents=True, exist_ok=True)
        dest.write_text(content)

        # Make .sh files executable
        exec_exts = (".sh", ".plist")
        exec_prefixes = ("install", "uninstall")
        if filename.endswith(exec_exts) or filename.startswith(exec_prefixes):
            dest.chmod(dest.stat().st_mode | stat.S_IXUSR | stat.S_IXGRP | stat.S_IXOTH)

        written.append(str(dest))

    return written


def get_schedule_status(platform_name: str = "") -> dict[str, Any]:
    """Get current schedule status: installed configs and next run times.

    Returns a dict with platform, installed services, last run, next run info.
    """
    if not platform_name:
        platform_name = detect_platform()

    platform_name = platform_name.lower()
    status: dict[str, Any] = {
        "platform": platform_name,
        "installed": [],
        "last_run": None,
        "next_run": None,
    }

    if platform_name == "launchd":
        import subprocess

        for label in ["com.openeraseme.tick", "com.openeraseme.poll", "com.openeraseme.rescan"]:
            plist_path = Path.home() / "Library" / "LaunchAgents" / f"{label}.plist"
            status["installed"].append({
                "label": label,
                "installed": plist_path.exists(),
                "path": str(plist_path) if plist_path.exists() else "",
            })

    elif platform_name == "systemd":
        import subprocess

        for name in ["openeraseme-tick", "openeraseme-poll", "openeraseme-rescan"]:
            try:
                result = subprocess.run(
                    ["systemctl", "--user", "is-enabled", f"{name}.timer"],
                    capture_output=True, text=True, timeout=10,
                )
                enabled = result.returncode == 0
            except (FileNotFoundError, subprocess.TimeoutExpired):
                enabled = False
            status["installed"].append({
                "label": name,
                "installed": enabled,
                "path": f"{name}.timer",
            })

    else:
        # cron: check crontab for our entries
        import subprocess

        try:
            result = subprocess.run(
                ["crontab", "-l"],
                capture_output=True, text=True, timeout=10,
            )
            has_entries = "openeraseme" in result.stdout
            status["installed"].append({
                "label": "cron",
                "installed": has_entries,
                "path": "crontab",
            })
        except (FileNotFoundError, subprocess.TimeoutExpired):
            status["installed"].append({
                "label": "cron",
                "installed": False,
                "error": "crontab not available",
            })

    return status
