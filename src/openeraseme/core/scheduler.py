def _plist_content(
    label: str,
    wrapper_path: str,
    schedule_interval: str,
    description: str = "",
) -> str:
    """Generate a launchd .plist file content.

    schedule_interval: e.g. "<hourly/>" or "<daily/>" or a calendar interval dict.
    """
    return f"""<?xml version="1.0" encoding="UTF-8"?>
<!DOCTYPE plist PUBLIC "-//Apple//DTD PLIST 1.0//EN"
  "http://www.apple.com/DTDs/PropertyList-1.0.dtd">
<plist version="1.0">
<dict>
    <key>Label</key>
    <string>{label}</string>
    <key>ProgramArguments</key>
    <array>
        <string>/bin/bash</string>
        <string>{wrapper_path}</string>
    </array>
    <key>StartCalendarInterval</key>
    {schedule_interval}
    <key>StandardOutPath</key>
    <string>/tmp/{label}.log</string>
    <key>StandardErrorPath</key>
    <string>/tmp/{label}.err</string>
    <key>EnvironmentVariables</key>
    <dict>
        <key>OPENERASEME_HEADLESS</key>
        <string>1</string>
    </dict>
    <key>RunAtLoad</key>
    <false/>
    <key>KeepAlive</key>
    <false/>
</dict>
</plist>"""


def _plist_calendar(hour: int, minute: int) -> str:
    return f"""    <dict>
        <key>Hour</key>
        <integer>{hour}</integer>
        <key>Minute</key>
        <integer>{minute}</integer>
    </dict>"""


def generate_launchd(
    config: SchedulerConfig,
    project_dir: str = "",
) -> dict[str, str]:
    """Generate launchd .plist files and wrapper scripts.

    Returns a dict mapping filename -> content.
    """
    bin_path = config.openeraseme_bin or _resolve_bin()
    venv = config.venv_activate or _resolve_venv()
    out: dict[str, str] = {}

    # ── tick (daily) ──
    tick_cmd = f"{bin_path} tick --output json"
    tick_sh = _wrapper_script(tick_cmd, project_dir=project_dir, venv_activate=venv)
    out["openeraseme-tick.sh"] = tick_sh

    tick_calendar = _plist_calendar(config.tick_time.hour, config.tick_time.minute)
    out["com.openeraseme.tick.plist"] = _plist_content(
        label="com.openeraseme.tick",
        wrapper_path="__WRAPPER_DIR__/openeraseme-tick.sh",
        schedule_interval=tick_calendar,
        description="OpenEraseMe daily tick",
    )

    # ── poll-inbox (multiple times daily) ──
    poll_cmd = f"{bin_path} poll-inbox --output json"
    poll_sh = _wrapper_script(poll_cmd, project_dir=project_dir, venv_activate=venv)
    out["openeraseme-poll.sh"] = poll_sh

    # launchd supports StartCalendarInterval as an array of dicts
    poll_entries = "\n".join(
        _plist_calendar(t.hour, t.minute) for t in config.poll_times
    )
    out["com.openeraseme.poll.plist"] = f"""<?xml version="1.0" encoding="UTF-8"?>
<!DOCTYPE plist PUBLIC "-//Apple//DTD PLIST 1.0//EN"
  "http://www.apple.com/DTDs/PropertyList-1.0.dtd">
<plist version="1.0">
<dict>
    <key>Label</key>
    <string>com.openeraseme.poll</string>
    <key>ProgramArguments</key>
    <array>
        <string>/bin/bash</string>
        <string>__WRAPPER_DIR__/openeraseme-poll.sh</string>
    </array>
    <key>StartCalendarInterval</key>
    <array>
{poll_entries}
    </array>
    <key>StandardOutPath</key>
    <string>/tmp/com.openeraseme.poll.log</string>
    <key>StandardErrorPath</key>
    <string>/tmp/com.openeraseme.poll.err</string>
    <key>EnvironmentVariables</key>
    <dict>
        <key>OPENERASEME_HEADLESS</key>
        <string>1</string>
    </dict>
    <key>RunAtLoad</key>
    <false/>
    <key>KeepAlive</key>
    <false/>
</dict>
</plist>"""

    # ── re-scan (quarterly) ──
    rescan_cmd = f"{bin_path} tick --output json"
    rescan_sh = _wrapper_script(rescan_cmd, project_dir=project_dir, venv_activate=venv)
    out["openeraseme-rescan.sh"] = rescan_sh

    # Quarterly: Jan 1, Apr 1, Jul 1, Oct 1
    quarter_dates = [
        f"""    <dict>
        <key>Month</key>
        <integer>{m}</integer>
        <key>Day</key>
        <integer>1</integer>
        <key>Hour</key>
        <integer>{config.tick_time.hour}</integer>
        <key>Minute</key>
        <integer>{config.tick_time.minute}</integer>
    </dict>"""
        for m in [1, 4, 7, 10]
    ]

    out["com.openeraseme.rescan.plist"] = f"""<?xml version="1.0" encoding="UTF-8"?>
<!DOCTYPE plist PUBLIC "-//Apple//DTD PLIST 1.0//EN"
  "http://www.apple.com/DTDs/PropertyList-1.0.dtd">
<plist version="1.0">
<dict>
    <key>Label</key>
    <string>com.openeraseme.rescan</string>
    <key>ProgramArguments</key>
    <array>
        <string>/bin/bash</string>
        <string>__WRAPPER_DIR__/openeraseme-rescan.sh</string>
    </array>
    <key>StartCalendarInterval</key>
    <array>
{chr(10).join(quarter_dates)}
    </array>
    <key>StandardOutPath</key>
    <string>/tmp/com.openeraseme.rescan.log</string>
    <key>StandardErrorPath</key>
    <string>/tmp/com.openeraseme.rescan.err</string>
    <key>EnvironmentVariables</key>
    <dict>
        <key>OPENERASEME_HEADLESS</key>
        <string>1</string>
    </dict>
    <key>RunAtLoad</key>
    <false/>
    <key>KeepAlive</key>
    <false/>
</dict>
</plist>"""

    # ── Install / uninstall helpers ──
    install_script = """#!/usr/bin/env bash
set -euo pipefail
SCRIPT_DIR="$(cd "$(dirname "$0")" && pwd)"

install_plist() {
    local src="$1"
    local name
    name=$(basename "$src")
    local dest="$HOME/Library/LaunchAgents/$name"

    # Replace wrapper dir placeholder
    sed "s|__WRAPPER_DIR__|$SCRIPT_DIR|g" "$src" > /tmp/"$name"
    cp /tmp/"$name" "$dest"
    chmod 644 "$dest"
    chmod +x "$SCRIPT_DIR/openeraseme-tick.sh"
    chmod +x "$SCRIPT_DIR/openeraseme-poll.sh"
    chmod +x "$SCRIPT_DIR/openeraseme-rescan.sh"

    launchctl load "$dest" 2>/dev/null || true
    launchctl start "$(basename "$name" .plist)" 2>/dev/null || true
    echo "Installed: $name"
}

for plist in "$SCRIPT_DIR"/*.plist; do
    [ -f "$plist" ] && install_plist "$plist"
done
echo ""
echo "launchd jobs installed. Wrappers in: $SCRIPT_DIR"
echo "Logs: /tmp/com.openeraseme.*.log"
"""
    out["install.sh"] = install_script

    uninstall_script = """#!/usr/bin/env bash
set -euo pipefail
SCRIPT_DIR="$(cd "$(dirname "$0")" && pwd)"

for plist in "$SCRIPT_DIR"/*.plist; do
    [ -f "$plist" ] || continue
    name=$(basename "$plist" .plist)
    dest="$HOME/Library/LaunchAgents/$(basename "$plist")"

    if [ -f "$dest" ]; then
        launchctl unload "$dest" 2>/dev/null || true
        rm -f "$dest"
        echo "Removed: $name"
    fi
done
echo "launchd jobs uninstalled."
"""
    out["uninstall.sh"] = uninstall_script

    return out


