def _systemd_service(
    description: str,
    wrapper_path: str,
    user_service: bool = True,
) -> str:
    """Generate a systemd .service file."""
    return f"""[Unit]
Description={description}
After=network.target

[Service]
Type=oneshot
ExecStart=/bin/bash {wrapper_path}
Environment=OPENERASEME_HEADLESS=1
StandardOutput=journal
StandardError=journal

[Install]
WantedBy=default.target
"""


def _systemd_timer(
    description: str,
    on_calendar: str,
) -> str:
    """Generate a systemd .timer file."""
    return f"""[Unit]
Description={description}

[Timer]
OnCalendar={on_calendar}
Persistent=true

[Install]
WantedBy=timers.target
"""


def generate_systemd(
    config: SchedulerConfig,
    project_dir: str = "",
) -> dict[str, str]:
    """Generate systemd service + timer pairs and wrapper scripts.

    Returns a dict mapping filename -> content.
    """
    bin_path = config.openeraseme_bin or _resolve_bin()
    venv = config.venv_activate or _resolve_venv()
    out: dict[str, str] = {}

    # ── tick (daily) ──
    tick_cmd = f"{bin_path} tick --output json"
    out["openeraseme-tick.sh"] = _wrapper_script(
        tick_cmd, project_dir=project_dir, venv_activate=venv
    )
    out["openeraseme-tick.service"] = _systemd_service(
        "OpenEraseMe daily tick",
        "__WRAPPER_DIR__/openeraseme-tick.sh",
    )
    out["openeraseme-tick.timer"] = _systemd_timer(
        "OpenEraseMe daily tick",
        f"Daily-{config.tick_time.hour:02d}:{config.tick_time.minute:02d}",
    )

    # ── poll-inbox (hourly) ──
    poll_cmd = f"{bin_path} poll-inbox --output json"
    out["openeraseme-poll.sh"] = _wrapper_script(
        poll_cmd, project_dir=project_dir, venv_activate=venv
    )
    out["openeraseme-poll.service"] = _systemd_service(
        "OpenEraseMe hourly inbox poll",
        "__WRAPPER_DIR__/openeraseme-poll.sh",
    )
    out["openeraseme-poll.timer"] = _systemd_timer(
        "OpenEraseMe hourly inbox poll",
        "Hourly",
    )

    # ── re-scan (quarterly) ──
    rescan_cmd = f"{bin_path} tick --output json"
    out["openeraseme-rescan.sh"] = _wrapper_script(
        rescan_cmd, project_dir=project_dir, venv_activate=venv
    )
    out["openeraseme-rescan.service"] = _systemd_service(
        "OpenEraseMe quarterly re-scan",
        "__WRAPPER_DIR__/openeraseme-rescan.sh",
    )
    out["openeraseme-rescan.timer"] = _systemd_timer(
        "OpenEraseMe quarterly re-scan",
        "Quarterly",
    )

    # ── Install / uninstall helpers ──
    install_script = """#!/usr/bin/env bash
set -euo pipefail
SCRIPT_DIR="$(cd "$(dirname "$0")" && pwd)"
USER_MODE="${1:---user}"

chmod +x "$SCRIPT_DIR"/*.sh

for unit in "$SCRIPT_DIR"/*.service "$SCRIPT_DIR"/*.timer; do
    [ -f "$unit" ] || continue
    name=$(basename "$unit")
    dest="${XDG_CONFIG_HOME:-$HOME/.config}/systemd/user/$name"
    mkdir -p "$(dirname "$dest")"
    sed "s|__WRAPPER_DIR__|$SCRIPT_DIR|g" "$unit" > "$dest"
    echo "Installed: $name"
done

systemctl $USER_MODE daemon-reload
systemctl $USER_MODE enable --now openeraseme-tick.timer
systemctl $USER_MODE enable --now openeraseme-poll.timer
systemctl $USER_MODE enable --now openeraseme-rescan.timer
echo ""
echo "Systemd timers installed and enabled."
echo "Wrappers in: $SCRIPT_DIR"
echo ""
systemctl $USER_MODE list-timers | grep -i openeraseme || true
"""
    out["install.sh"] = install_script

    uninstall_script = """#!/usr/bin/env bash
set -euo pipefail
USER_MODE="${1:---user}"

for unit in openeraseme-tick openeraseme-poll openeraseme-rescan; do
    systemctl $USER_MODE disable --now "$unit.timer" 2>/dev/null || true
done

for unit in "$SCRIPT_DIR"/*.service "$SCRIPT_DIR"/*.timer; do
    [ -f "$unit" ] || continue
    name=$(basename "$unit")
    dest="${XDG_CONFIG_HOME:-$HOME/.config}/systemd/user/$name"
    rm -f "$dest"
    echo "Removed: $name"
done

systemctl $USER_MODE daemon-reload
echo "Systemd timers uninstalled."
"""
    # Add SCRIPT_DIR to the uninstall script
    uninstall_script_fixed = (
        'SCRIPT_DIR="$(cd "$(dirname "$0")" && pwd)"\n\n'
        + uninstall_script
    )
    out["install.sh"] = install_script
    out["uninstall.sh"] = uninstall_script_fixed

    return out


