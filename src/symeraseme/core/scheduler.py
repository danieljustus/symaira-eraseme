# Wrapper script
# ---------------------------------------------------------------------------

WRAPPER_HEADER = """#!/usr/bin/env bash
# Symaira EraseMe scheduled task wrapper
# Generated by symeraseme generate-scheduler
set -euo pipefail

{venv_block}

export SYMERASEME_HEADLESS=1
export PATH="${{PATH}}:/usr/local/bin:/opt/homebrew/bin"

cd {project_dir}

{command}
"""


def _wrapper_script(
    command: str,
    project_dir: str = "",
    venv_activate: str = "",
) -> str:
    """Generate a shell wrapper script for a scheduler command."""
    if not project_dir:
        project_dir = str(Path.cwd())
    if not venv_activate:
        venv_activate = _resolve_venv()

    venv_block = f"source {shlex.quote(venv_activate)}" if venv_activate else ""

    return WRAPPER_HEADER.format(
        venv_block=venv_block,
        project_dir=shlex.quote(project_dir),
        command=command,
    )


