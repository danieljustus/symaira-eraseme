def generate_cron(
    config: SchedulerConfig,
    project_dir: str = "",
) -> dict[str, str]:
    """Generate cron tab entries and wrapper scripts.

    Returns a dict mapping filename -> content.
    """
    bin_path = config.symeraseme_bin or _resolve_bin()
    venv = config.venv_activate or _resolve_venv()
    out: dict[str, str] = {}

    # ── tick (daily) ──
    tick_cmd = f"{bin_path} tick --output json"
    out["symeraseme-tick.sh"] = _wrapper_script(
        tick_cmd, project_dir=project_dir, venv_activate=venv
    )

    # ── poll-inbox (hourly via wrapper with time check) ──
    # Since cron doesn't handle multiple daily times well, we check in the wrapper
    poll_times_str = "|".join(f"{t.hour:02d}:{t.minute:02d}" for t in config.poll_times)
    poll_wrapper = f"""#!/usr/bin/env bash
set -euo pipefail

{('source "' + venv + '"') if venv else ""}
export SYMERASEME_HEADLESS=1
export PATH="${{PATH}}:/usr/local/bin:/opt/homebrew/bin"
cd "{project_dir}"

# Only run at configured poll times
CURRENT_HHMM=$(date +%H:%M)
case "$CURRENT_HHMM" in
{poll_times_str})
    {bin_path} poll-inbox --output json
    ;;
esac
"""
    out["symeraseme-poll.sh"] = poll_wrapper

    # ── re-scan (quarterly) ──
    rescan_cmd = f"{bin_path} tick --output json --dry-run"
    # Re-scan runs quarterly (every 3 months)
    out["symeraseme-rescan.sh"] = _wrapper_script(
        rescan_cmd, project_dir=project_dir, venv_activate=venv
    )

    # ── Crontab entries ──
    crontab_lines = [
        "# Symaira EraseMe scheduled tasks",
        f"# Generated by symeraseme generate-scheduler on {datetime.now().isoformat()}",
        "",
        f"# Tick (daily at {config.tick_time.hour:02d}:{config.tick_time.minute:02d})",
        "{} {} * * * {}/symeraseme-tick.sh".format(
            config.tick_time.minute,
            config.tick_time.hour,
            "{wrapper_dir}",
        ),
        "",
        "# Poll inbox (every hour, wrapper checks configured times)",
        "0 * * * * {wrapper_dir}/symeraseme-poll.sh".format(wrapper_dir="{wrapper_dir}"),
        "",
        "# Re-scan (quarterly on 1st of Jan, Apr, Jul, Oct)",
        "{min} {hour} 1 1,4,7,10 * {wrapper_dir}/symeraseme-rescan.sh".format(
            min=config.tick_time.minute,
            hour=config.tick_time.hour,
            wrapper_dir="{wrapper_dir}",
        ),
        "",
    ]
    out["crontab.txt"] = "\n".join(crontab_lines)

    # ── Install / uninstall helpers ──
    install_script = """#!/usr/bin/env bash
set -euo pipefail
SCRIPT_DIR="$(cd "$(dirname "$0")" && pwd)"

# Make wrappers executable
chmod +x "$SCRIPT_DIR"/*.sh

# Install crontab entries (replace our block)
TEMP=$(mktemp)
crontab -l 2>/dev/null | awk '
  /^# Symaira EraseMe scheduled tasks$/ { skip=1; next }
  /^# End Symaira EraseMe scheduled tasks$/ { skip=0; next }
  skip { next }
  { print }
' > "$TEMP" 2>/dev/null || true

cat "$SCRIPT_DIR/crontab.txt" >> "$TEMP"
# Replace wrapper_dir placeholder
sed -i '' "s|{wrapper_dir}|$SCRIPT_DIR|g" "$TEMP" 2>/dev/null || \
sed -i "s|{wrapper_dir}|$SCRIPT_DIR|g" "$TEMP"

if crontab "$TEMP" 2>/dev/null; then
    echo "Crontab installed. Wrappers in: $SCRIPT_DIR"
else
    echo "Warning: crontab install failed. Manual installation:"
    echo "  crontab $TEMP"
fi
rm -f "$TEMP"
"""
    out["install.sh"] = install_script

    uninstall_script = """#!/usr/bin/env bash
set -euo pipefail

TEMP=$(mktemp)
crontab -l 2>/dev/null | awk '
  /^# Symaira EraseMe scheduled tasks$/ { skip=1; next }
  /^# End Symaira EraseMe scheduled tasks$/ { skip=0; next }
  skip { next }
  { print }
' > "$TEMP" 2>/dev/null || true

if crontab "$TEMP" 2>/dev/null; then
    echo "Symaira EraseMe entries removed from crontab."
else
    echo "No crontab to modify."
fi
rm -f "$TEMP"
"""
    out["uninstall.sh"] = uninstall_script

    return out


